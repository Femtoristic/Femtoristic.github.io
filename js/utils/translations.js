// Translation utility function
function applyTranslations(lang) {
  const elements = document.querySelectorAll('[data-i18n]');
  elements.forEach(element => {
    const key = element.getAttribute('data-i18n');
    const value = getNestedValue(window.translations[lang], key);
    if (value !== undefined) {
      if (element.tagName === 'INPUT' && element.type === 'placeholder') {
        element.placeholder = value;
      } else {
        element.innerHTML = value;
      }
    }
  });
}

// Helper function to get nested object values
function getNestedValue(obj, path) {
  return path.split('.').reduce((current, key) => {
    return current && current[key] !== undefined ? current[key] : undefined;
  }, obj);
}

// Initialize translations on page load
document.addEventListener('DOMContentLoaded', function() {
  // Set initial language (German by default)
  applyTranslations('de');
  
  // Add language switcher functionality
  document.querySelectorAll('.lang-btn, .mobile-lang-btn').forEach(btn => {
    btn.addEventListener('click', function() {
      const lang = this.getAttribute('data-lang');
      
      // Update active state
      document.querySelectorAll('.lang-btn, .mobile-lang-btn').forEach(b => b.classList.remove('active'));
      this.classList.add('active');
      
      // Apply translations
      applyTranslations(lang);
    });
  });
}); 